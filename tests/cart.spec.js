/* global describe beforeEach it */

const {expect} = require('chai')
const db = require('../server/db')
const Cart = db.model('cart')

var cart;
beforeEach(function(){
  cart = Cart.build({
    subTotal: 12.23,
    total: 15.12
  });
});

describe('Cart model: attributes definition', function(){

      it('includes `subTotal` and `total` fields', function () {

        return cart.save()
        .then(function (savedCart) {
          expect(savedCart.subTotal).to.equal('12.23');
          expect(savedCart.total).to.equal('15.12');
        });

      });
      });


